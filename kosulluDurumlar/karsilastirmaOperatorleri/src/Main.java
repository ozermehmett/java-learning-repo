public class Main {
    public static void main(String[] args) {
        /*
        Karşılaştırma operatörleri

        ==  ---> Eşit mi?
        !=  ---> Eşit değil mi?
        >   ---> Büyük mü?
        >=  ---> Büyük veya Eşit mi?
        <   ---> Küçük mü?
        <=  ---> Küçük veya Eşit mi?
        */

        System.out.println(3 == 4); //false
        System.out.println(3 < 5); //true
        System.out.println("Mehmet" == "Mehmet"); //true
        // System.out.println("Mehmet" < "Mehmet"); //ERROR!!!!!

    }
}